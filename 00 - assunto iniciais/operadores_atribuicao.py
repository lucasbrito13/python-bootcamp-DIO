# atribuição simples
saldo = 500
print(saldo)

saldo = 200
print(saldo)

# Atribuição com Adição
saldo += 10 
print(saldo)

# Atribuição com Subtração
saldo -= 5
print(saldo)

# Atribuição com Divisão sem ponto flutuante
saldo //= 2
print(saldo)

# Atribuição com Divisão com ponto flutuante
saldo /= 2
print(saldo)

# Atribuição com Multiplicação
saldo *= 10
print(saldo)

# Atribuição com Módulo
saldo %= 4
print(saldo)

# Atribuição com Expoenciação
saldo **= 2
print(saldo)
 